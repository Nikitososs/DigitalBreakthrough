"""Тесты форматирования телефонов МО."""

from __future__ import annotations

import pytest

from app.agency_mapping import resolve_municipality_admin
from app.phone_format import format_phone_ru


@pytest.mark.parametrize(
    ("raw", "expected"),
    [
        ("8(38141)2-26-26", "+7 (38141) 2-26-26"),
        ("83816921144", "+7 (38169) 2-11-44"),
        ("+7 (38162) 2-21-02", "+7 (38162) 2-21-02"),
        ("8 (38179) 22622", "+7 (38179) 2-26-22"),
        ("+7(38173)21-642", "+7 (38173) 2-16-42"),
        ("+7-38167-21119", "+7 (38167) 2-11-19"),
        ("8(38168) 2-14-98 -", "+7 (38168) 2-14-98"),
        ("+7 (381) 582-16-42", "+7 (381) 582-16-42"),
        ("+7 904 821 87 57", "+7 (904) 821-87-57"),
        ("+73817623242", "+7 (38176) 2-32-42"),
        ("+7 (3812) 78-78-20 (", "+7 (3812) 78-78-20"),
        ("+7 (3812) 23-18-90", "+7 (3812) 23-18-90"),
        ("+7 (38157) 21-285", "+7 (38157) 21-285"),
        ("8 (38166)22-166", "+7 (38166) 22-166"),
        ("+7(38151)2-44-10", "+7 (38151) 2-44-10"),
        ("+7 (38154)2-16-48", "+7 (38154) 2-16-48"),
        ("+7-38153-21456", "+7 (38153) 2-14-56"),
        ("+7 (38150) 2-17-00", "+7 (38150) 2-17-00"),
        ("8(38159)2-14-00", "+7 (38159) 2-14-00"),
        ("8(38163)21-330", "+7 (38163) 21-330"),
        ("8 (38178) 2 12 32", "+7 (38178) 2-12-32"),
        ("+7(38164)21401", "+7 (38164) 2-14-01"),
        ("83816021235", "+7 (38160) 2-12-35"),
        ("+7-38172-31305", "+7 (38172) 3-13-05"),
        ("+7 (38155) 27-112", "+7 (38155) 27-112"),
        ("+7 (381-61) 2-17-17", "+7 (38161) 2-17-17"),
        ("8 (38 165) 2-31-01", "+7 (38165) 2-31-01"),
        ("+7 (38175) 2-19-85", "+7 (38175) 2-19-85"),
        ("8(38174)2-25-25 - ", "+7 (38174) 2-25-25"),
        ("+7(38173)21-642, +7 904 821 87 57", "+7 (38173) 2-16-42, +7 (904) 821-87-57"),
        ("+7 (3812) 95-44-44 доб. 123", "+7 (3812) 95-44-44 доб. 123"),
    ],
)
def test_format_phone_ru_examples(raw: str, expected: str) -> None:
    assert format_phone_ru(raw) == expected


def test_format_phone_ru_unparseable_returns_cleaned() -> None:
    garbled = "85-43-13-2 5-24 16-19 38-30 53-26 239-24 82 1 135 13 20 5 33"
    assert format_phone_ru(garbled) == garbled
    assert format_phone_ru("8 718 512 479 429 366 314") == "8 718 512 479 429 366 314"


def test_format_phone_ru_none_and_empty() -> None:
    assert format_phone_ru(None) is None
    assert format_phone_ru("") is None
    assert format_phone_ru("   ") is None


def test_resolve_municipality_admin_formats_phone() -> None:
    omsk = resolve_municipality_admin("г. Омск")
    assert omsk["phone"] == "+7 (3812) 78-78-20"

    bol = resolve_municipality_admin("Большереченский район")
    assert bol["phone"] == "+7 (38169) 2-11-44"
